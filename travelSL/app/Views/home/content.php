        <div class="info m-4">
            <p>Sri Lanka, formerly known as Ceylon, and officially the Democratic Socialist Republic of Sri Lanka, is an
                island country in South Asia. It lies in the Indian Ocean, southwest of the Bay of Bengal, and southeast
                of
                the Arabian Sea; it is separated from the Indian subcontinent by the Gulf of Mannar and the Palk Strait.
                Sri
                Lanka shares a maritime border with India and the Maldives. Sri Jayawardenepura Kotte is its legislative
                capital, and Colombo is its largest city and financial centre.</p>

            <p>Sri Lanka's documented history goes back 3,000 years, with evidence of prehistoric human settlements that
                dates to at least 125,000 years ago. It has a rich cultural heritage</p>

            <p>Today, Sri Lanka is a multinational state, home to diverse cultures, languages, and ethnicities. The
                Sinhalese are the majority of the nation's population. The Tamils, who are a large minority group, have
                also
                played an influential role in the island's history. Other long established groups include the Moors, the
                Burghers, the Malays, the Chinese, and the indigenous Vedda.</p>
